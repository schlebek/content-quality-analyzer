/* ── MO binary generator ────────────────────────────────────── */

function makeMO( msgids, translations, header ) {
	const entries = msgids.map( id => ( {
		id:  Buffer.from( id,                       'utf8' ),
		str: Buffer.from( translations[ id ] || id, 'utf8' ),
	} ) );

	entries.sort( ( a, b ) => a.id.compare( b.id ) );

	const headerIdBuf  = Buffer.alloc( 0 );
	const headerStrBuf = Buffer.from( header, 'utf8' );

	const allEntries = [ { id: headerIdBuf, str: headerStrBuf }, ...entries ];
	const n          = allEntries.length;
	const headerSize = 28;
	const tableSize  = n * 8;
	const stringsOff = headerSize + tableSize * 2;

	const origTable  = [];
	const transTable = [];
	const strings    = [];
	let offset       = stringsOff;

	for ( const e of allEntries ) {
		origTable.push( { len: e.id.length, off: offset } );
		strings.push( e.id );
		strings.push( Buffer.alloc( 1 ) );
		offset += e.id.length + 1;
	}
	for ( const e of allEntries ) {
		transTable.push( { len: e.str.length, off: offset } );
		strings.push( e.str );
		strings.push( Buffer.alloc( 1 ) );
		offset += e.str.length + 1;
	}

	const totalSize = offset;
	const buf       = Buffer.alloc( totalSize );

	buf.writeUInt32LE( 0x950412de, 0 );
	buf.writeUInt32LE( 0,          4 );
	buf.writeUInt32LE( n,          8 );
	buf.writeUInt32LE( headerSize,               12 );
	buf.writeUInt32LE( headerSize + tableSize,   16 );
	buf.writeUInt32LE( 0,          20 );
	buf.writeUInt32LE( 0,          24 );

	let tOff = headerSize;
	for ( const t of origTable ) {
		buf.writeUInt32LE( t.len, tOff );
		buf.writeUInt32LE( t.off, tOff + 4 );
		tOff += 8;
	}
	for ( const t of transTable ) {
		buf.writeUInt32LE( t.len, tOff );
		buf.writeUInt32LE( t.off, tOff + 4 );
		tOff += 8;
	}

	let sOff = stringsOff;
	for ( const s of strings ) {
		s.copy( buf, sOff );
		sOff += s.length;
	}

	return buf;
}

